def initialization_params(dims):
    params = {}
    n = len(dims)

    for i in range(n-1):
        params["W" + str(i+1)] = np.random.randn(dims[i+1], dims[i]) * 0.01
        params["b" + str(i+1)] = np.zeros((dims[i+1], 1))

    return params

